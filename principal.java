    public ArrayList<Produkto> getProductosList(){
        
        ArrayList<Produkto> list = new ArrayList<>();
        
        String selectQuery = "SELECT * FROM productos_BdD.productos;";
        
        
        Statement s;
        ResultSet r;
        
        try {
            s = Base_dDatos.getConnection().createStatement();
            
            r = s.executeQuery(selectQuery);
            
            Produkto produkto;
            
            while (r.next()){
                produkto = new Produkto(r.getInt("id"), r.getString("nombre"), r.getString("raiz"), r.getInt("cantidad"), r.getDouble("precio"), r.getInt("disponibilidad"));
                
                list.add(produkto);
            
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(principal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        produktosf = list;
        return list;
        
        
    }
    
    
    
    public void muestradProduktos(){
        
        ArrayList<Produkto> produktoslist = getProductosList();
        
        DefaultTableModel d = (DefaultTableModel) jTable_productos.getModel();

        d.setRowCount(0);
        
        Object[] fila = new Object[6];
        
        for (int i = 0; i < produktoslist.size(); i++) {
            
            fila[0] =  produktoslist.get(i).getId();
            fila[1] =  produktoslist.get(i).getNombre();
            fila[2] =  produktoslist.get(i).getRaiz();
            fila[4] =  produktoslist.get(i).getPrecio();
            fila[3] =  produktoslist.get(i).getCantidad();
            fila[5] =  produktoslist.get(i).getDisponibilidad();
            
        d.addRow(fila);
            
            
        }
        
         
        
    }
    
    
