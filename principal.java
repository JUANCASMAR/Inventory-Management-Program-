    public void enseñodProduktos (int index){
       
        jSpinner_myu.setValue(getProductosList().get(index).getId());
        jTextField_nombre.setText(getProductosList().get(index).getNombre());
        jComboBox_raiz.setSelectedItem(getProductosList().get(index).getRaiz());;
        jTextField_cantidad.setText(getProductosList().get(index).getCantidad().toString());
        
        Double g = getProductosList().get(index).getPrecio();
        jTextField_precio.setText(g+"");
   
        Integer j = getProductosList().get(index).getDisponibilidad();
        jTextField_disp.setText(j+"");
        
    }
    
